    private void loadPropertiesToTable() {
        DefaultTableModel model = (DefaultTableModel) propertyTable.getModel();
        model.setRowCount(0); // Clear existing rows
        
        Integer blockFilter;
        if(selectedBlock == 0) {
            blockFilter = null;
        }
        else {
            blockFilter = selectedBlock;
        }
        
        Double minPrice = parseDoubleOrNull(minPriceField.getText());
        Double maxPrice = parseDoubleOrNull(maxPriceField.getText());
        Double minSize = parseDoubleOrNull(minSizeField.getText());
        Double maxSize = parseDoubleOrNull(maxSizeField.getText());
        
        for (Block block : customer.getAllBlocks()) {
            if (blockFilter != null) {
                if (block.getBlockNumber() != blockFilter) {
                    continue;
                }
            }

            for (Property property : block.getProperties()) {
                if (minPrice != null) {
                    if (property.getContactPrice() < minPrice) {
                        continue;
                    }
                }

                if (maxPrice != null) {
                    if (property.getContactPrice() > maxPrice) {
                        continue;
                    }
                }

                if (minSize != null) {
                    if (property.getPropertySize() < minSize) {
                        continue;
                    }
                }

                if (maxSize != null) {
                    if (property.getPropertySize() > maxSize) {
                        continue;
                    }
                }

                String type;
                if (property instanceof TownHouse) {
                    type = "Town House";
                } else if (property instanceof SemiDetached) {
                    type = "Semi-Detached";
                } else if (property instanceof Detached) {
                    type = "Detached";
                } else {
                    type = "";
                }

                model.addRow(new Object[]{
                    property.getPropertyId(),
                    block.getBlockNumber(),
                    property.getPropertyNumber(),
                    property.getStatus(),
                    property.getOwner(),
                    property.getContactPrice(),
                    property.getPropertySize(),
                    property.getFloors(),
                    type
                });
            }
        }
    }
    
    private Double parseDoubleOrNull(String text){
        if (text == null || text.isEmpty())
            return null;
        if (!text.matches("\\d+(\\.\\d+)?"))
            return null;
        return Double.parseDouble(text);
    }
    
    private boolean isInvalidPrice(String text, Double price) {
        return text != null && !text.isEmpty() && price == null;
    }
    
    private void filterProperties(){
        double minPrice = parseDoubleOrNull(minPriceField.getText());
        double maxPrice = parseDoubleOrNull(maxPriceField.getText());
        
        // If minimum price is invalid
        if (isInvalidPrice(minPriceField.getText(), minPrice)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Enter a valid numeric value for minimum price.");
            return;
        }

        // Check if the maximum price is invalid
        if (isInvalidPrice(maxPriceField.getText(), maxPrice)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Enter a valid numeric value for maximum price.");
            return;
        }
        
        loadPropertiesToTable();
    }
        
    private void loadOwnedPropertiesToTable() { 
        DefaultTableModel ownedModel = (DefaultTableModel) ownedPropertiesTable.getModel();
        ownedModel.setRowCount(0);
        
        // Ito sinasabi ko it must ask the PropertyManager for the blocks, not the Customer
        for(Block block : propertyManager.getAllBlocks()) { 
            for(Property property : block.getProperties()) { 
                
                Customer owner = property.getOwner();
                Customer reserved = property.getReservedBy();
                
                // This will check if the owner is NOT null before checking .equals()
                if ((owner != null && owner.equals(this.customer)) || (reserved != null && reserved.equals(this.customer))) { 

                    String type = property.getClass().getSimpleName();
                    String status;
                    
                    if (owner != null && owner.equals(this.customer)) {
                        status = "Owned";
                    } else if (reserved != null && reserved.equals(this.customer)) {
                        status = "Reserved";
                    } else {
                        status = property.getStatus();
                    }

                    ownedModel.addRow(new Object[] {
                        property.getPropertyId(),
                        block.getBlockNumber(),
                        property.getPropertyNumber(), 
                        status,
                        property.getContactPrice(),
                        property.getPropertySize(),
                        property.getFloors(),
                        type
                    });
                }
            }
        }
    }
    
    private void setupTable() {
        propertyTable.setRowSelectionAllowed(true);
        propertyTable.setSelectionMode(javax.swing.ListSelectionModel.SINGLE_SELECTION);
        
        propertyTable.getSelectionModel().addListSelectionListener(e -> {
            if (!e.getValueIsAdjusting()) {
                int row = propertyTable.getSelectedRow();

                if (row >= 0) { // a row is selected
                    int propertyId = (int) propertyTable.getValueAt(row, 0); // first column = propertyId
                    selectedProperty = customer.getProperty(propertyId);
                } else {
                    selectedProperty = null;
                }
            }
        });
    }
    
